public class NameInitials
{
  public static void main(String[] args)
  {
    String firstName="Adrian";
    String middleName="Cristian";
    String lastName="Militaru";

    char firstInitial = 'A';
    char middleInitial = 'C';
    char lastInitial='M';

    System.out.println("First name: "+firstName+' '+firstInitial);
    System.out.println("First name: "+middleName+' '+middleInitial);
    System.out.println("First name: "+lastName+' '+lastInitial);
  }
}
